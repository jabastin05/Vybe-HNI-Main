type ServiceInput = {
  id: string;
  name: string;
  categoryId: string;
  categoryName: string;
  description: string;
  iconName: string;
  eta?: string;
  priceRange?: string;
  requirements?: string[];
  deliverables?: string[];
  executionPartnerRole?: string;
  badge?: string;
  featured?: boolean;
};

const brandGradient = 'from-brand-primary to-brand-teal';

function createService(input: ServiceInput): ServiceData {
  return {
    id: input.id,
    name: input.name,
    categoryId: input.categoryId,
    categoryName: input.categoryName,
    description: input.description,
    iconName: input.iconName,
    color: brandGradient,
    badge: input.badge,
    featured: input.featured ?? false,
    attributes: [
      {
        name: input.name,
        description: input.description,
        eta: input.eta ?? '5-10 days',
        priceRange: input.priceRange ?? 'Custom pricing',
        requirements: input.requirements ?? ['Property details', 'Owner authorization', 'Engagement scope'],
        deliverables: input.deliverables ?? ['Advisory note', 'Findings summary', 'Recommended next steps'],
        executionPartnerRole: input.executionPartnerRole ?? 'VYBE Advisory',
        enabled: true,
      },
    ],
  };
}

